// 13-14: сравниваем два числа между собой и в выводе будет false, если неверно
// и true, если верно.
//
// 15-16: `==` проверяет равны ли два значения, `!=` проверяет не равны ли они. 
//
// 17-18: меньше иили равно и больше или равно одно число относительно другого.

package main

import "fmt"

func main() {
	fmt.Println(4 < 6)
	fmt.Println(4 > 6)
	fmt.Println(2+2 == 5)
	fmt.Println(2+2 != 5)
	fmt.Println(4 <= 6)
	fmt.Println(4 >= 4)
}
